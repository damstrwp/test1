import sqlite3

conn = sqlite3.connect('kinobot.db')
cursor = conn.cursor()
cursor.execute('''
    CREATE TABLE IF NOT EXISTS films(
        id INTEGER PRIMARY KEY,
        film TEXT,
        genre TEXT,
        year TEXT,
        country TEXT,
        director TEXT,
        about TEXT
    )

''')
conn.commit()


