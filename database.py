def get_film(genre, year):
    cursor.execute('''
        SELECT film,genre,year,country,director,about FROM films 
        WHERE genre = ? AND year = ?
    ''', (genre, year))  # Передаем параметры кортежем
    return cursor.fetchall()  # Возвращаем все найденные записи


